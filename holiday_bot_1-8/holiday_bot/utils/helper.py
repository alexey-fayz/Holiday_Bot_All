from datetime import datetime


def get_today_str() -> str:
    """
    Возвращает текущую дату в формате строки 'ГГГГ-ММ-ДД'.
    
    Returns:
        str: Строка с текущей датой в формате ГГГГ-ММ-ДД
        
    Examples:
        >>> get_today_str()
        '2024-03-15'  # если сегодня 15 марта 2024 года
    """
    # Получаем текущую дату
    today = datetime.now().date()
    
    # Форматируем дату в строку ГГГГ-ММ-ДД
    # strftime форматирует дату согласно заданным кодам:
    # %Y - год с веком (4 цифры)
    # %m - месяц (2 цифры, с ведущим нулем)
    # %d - день (2 цифры, с ведущим нулем)
    return today.strftime("%Y-%m-%d")


# Пример использования при запуске модуля напрямую
if __name__ == "__main__":
    print(f"Сегодня: {get_today_str()}")